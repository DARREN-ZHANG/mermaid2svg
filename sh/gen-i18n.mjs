// 生成文件内容
const genContent = (data) => {
  const entries = Object.entries(data).map(([k, v]) => {
    if (Array.isArray(v)) {
      const items = v.map((i) => "    " + JSON.stringify(i)).join(",\n");
      return "  " + k + ": [\n" + items + ",\n  ],";
    }
    return "  " + k + ": " + JSON.stringify(v) + ",";
  });
  return "export default () => ({\n" + entries.join("\n") + "\n});\n";
};

let generated = 0;
for (const code of CODE) {
  const data = I18N[code] || EN;
  const content = genContent(data);
  writeFileSync(join(DIR, code + ".js"), content);
  ++generated;
}

console.log("Generated " + generated + " locale files");
